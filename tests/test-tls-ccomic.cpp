		{
			return
				this->header +
				this->tile1() +
				this->tile2()
			;
		}

		virtual std::string rename()
		{
			throw stream::error("This tileset does not have any tilenames.");
		}

		virtual std::string insert_end()
		{
			return
				this->header +
				this->tile1() +
				this->tile2() +
				this->tile3()
			;
		}

		virtual std::string insert_mid()
		{
			return
				this->header +
				this->tile1() +
				this->tile3() +
				this->tile2()
			;
		}

		virtual std::string insert2()
		{
			return
				this->header +
				this->tile1() +
				this->tile3() +
				this->tile4() +
				this->tile2()
			;
		}

		virtual std::string remove()
		{
			return
				this->header +
				this->tile2()
			;
		}

		virtual std::string remove2()
		{
			return this->header;
		}

		virtual std::string insert_remove()
		{
			return
				this->header +
				this->tile3() +
				this->tile2()
			;
		}

		virtual std::string move()
		{
			return
				this->header +
				this->tile2() +
				this->tile1()
			;
		}

